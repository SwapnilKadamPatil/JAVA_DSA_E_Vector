package String_Programs;

public class String_Length {

	public static void main(String[] args) {
		
		String str1 = "abcdefghijk";
		String str2 = "ABSDFGHJKLZXCVBNNM";
		String str3 = "Java Programming";
		String str4 = "   ";
		String str5 = "";
	
	
		System.out.println("String 1 Length ="+ str1.length());
		System.out.println("String 2 Length ="+ str2.length());
		System.out.println("String 3 Length ="+ str3.length());
		System.out.println("String 4 Length ="+ str4.length());
		System.out.println("String 5 Length ="+ str5.length());
	
	}

}

